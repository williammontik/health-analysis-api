def compute_age(dob):
    try:
        dt = parser.parse(dob)
        today = datetime.today()
        return today.year - dt.year - ((today.month, today.day) < (dt.month, dt.day))
    except:
        return 0

def get_openai_response(prompt, temp=0.7):
    try:
        res = client.chat.completions.create(
            model="gpt-3.5-turbo",
            messages=[{"role": "user", "content": prompt}],
            temperature=temp
        )
        return res.choices[0].message.content
    except Exception as e:
        logging.error(f"OpenAI error: {e}")
        return "⚠️ Unable to generate response"

def send_email(html_body, lang):
    subject = LANGUAGE[lang]["email_subject"]
    msg = MIMEText(html_body, 'html', 'utf-8')
    msg['Subject'] = subject
    msg['From'] = SMTP_USERNAME
    msg['To'] = SMTP_USERNAME
    try:
        with smtplib.SMTP(SMTP_SERVER, SMTP_PORT) as server:
            server.starttls()
            server.login(SMTP_USERNAME, SMTP_PASSWORD)
            server.send_message(msg)
    except Exception as e:
        logging.error(f"Email send error: {e}")

@app.route("/health_analyze", methods=["POST"])
def health_analyze():
    try:
        data = request.get_json(force=True)
        lang = data.get("lang", "en").strip().lower()
        if lang not in LANGUAGE:
            lang = "en"

        labels = LANGUAGE_TEXTS[lang]
        content = LANGUAGE[lang]

        name = data.get("name")
        dob = data.get("dob")
        gender = data.get("gender")
        height = data.get("height")
        weight = data.get("weight")
        country = data.get("country")
        concern = data.get("condition")
        notes = data.get("details", "") or "No additional details"
        ref = data.get("referrer")
        angel = data.get("angel")
        age = compute_age(dob)

        prompts = {
            "summary": f"A {age}-year-old {gender} from {country} is experiencing '{concern}'. Description: {notes}. Write 4 paragraphs of advice in third-person. Avoid using 'you'. Use realistic or statistical phrasing.",
            "creative": f"As a health coach, give 10 lifestyle tips with emojis for a {age}-year-old {gender} from {country} dealing with '{concern}'. Notes: {notes}"
        }

        summary = get_openai_response(prompts["summary"])
        creative = get_openai_response(prompts["creative"], temp=0.85)

        html = f"<h4 style='text-align:center;'>{content['report_title']}</h4>"
        html += f"<p><strong>{labels['name']}:</strong> {name}<br>"
        html += f"<strong>{labels['dob']}:</strong> {dob}<br>"
        html += f"<strong>{labels['country']}:</strong> {country}<br>"
        html += f"<strong>{labels['gender']}:</strong> {gender}<br>"
        html += f"<strong>{labels['age']}:</strong> {age}<br>"
        html += f"<strong>{labels['height']}:</strong> {height}<br>"
        html += f"<strong>{labels['weight']}:</strong> {weight}<br>"
        html += f"<strong>{labels['concern']}:</strong> {concern}<br>"
        html += f"<strong>{labels['desc']}:</strong> {notes}<br>"
        html += f"<strong>{labels['ref']}:</strong> {ref}<br>"
        html += f"<strong>{labels['angel']}:</strong> {angel}</p>"

        html += f"<br><div style='font-size:24px; font-weight:bold; margin-top:30px;'>🧠 Summary:</div><br>"
        for para in summary.split("\n"):
            if para.strip():
                html += f"<p style='line-height:1.7; font-size:16px; margin-bottom:16px;'>{para.strip()}</p>"

        html += f"<br><div style='font-size:24px; font-weight:bold; margin-top:30px;'>💡 Creative Suggestions:</div><br>"
        for line in creative.split("\n"):
            if line.strip():
                html += f"<p style='margin:16px 0; font-size:17px;'>{line.strip()}</p>"

        html += (
            f"<br><br><p style='font-size:16px;'><strong>🛡️ Disclaimer:</strong></p>"
            f"<p style='font-size:15px; line-height:1.6;'>🩺 This platform offers general lifestyle suggestions. Please consult a licensed medical professional for diagnosis or treatment decisions.</p>"
        )

        html += f"<p style='color:#888; margin-top:20px;'>{labels['footer']}</p>"

        send_email(html, lang)

        return jsonify({
            "summary": summary,
            "creative": creative,
            "footer": labels['footer']
        })
    except Exception as e:
        logging.error(f"Health analyze error: {e}")
        return jsonify({"error": "Server error"}), 500

