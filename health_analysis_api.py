def generate_metrics_with_ai(prompt_text):
    try:
        is_zh = 'ç®€ä½“ä¸­æ–‡' in prompt_text or 'ç¹é«”' in prompt_text or 'è«‹ä½¿ç”¨' in prompt_text
        response = client.chat.completions.create(
            model="gpt-3.5-turbo",
            messages=[
                {"role": "system", "content": "ä½ æ˜¯ä¸€ä½åªç”¨ä¸­æ–‡å›ç­”çš„å¥åº·åŠ©ç†ã€‚" if is_zh else "You are a helpful assistant replying in English."},
                {"role": "user", "content": prompt_text}
            ],
            temperature=0.7
        )
        lines = response.choices[0].message.content.strip().split("\n")
        metrics = []
        current_title, labels, values = "", [], []
        for line in lines:
            if line.startswith("###"):
                if current_title and labels and values:
                    metrics.append({"title": current_title, "labels": labels, "values": values})
                current_title, labels, values = line[3:].strip(), [], []
            elif ":" in line:
                label, val = line.split(":", 1)
                labels.append(label.strip())
                try:
                    values.append(int(val.strip().replace("%", "")))
                except:
                    values.append(50)
        if current_title and labels and values:
            metrics.append({"title": current_title, "labels": labels, "values": values})
        return metrics
    except Exception as e:
        logging.warning(f"GPT metric error: {e}")
        return []

